// 데이터 불변성(Immutability)
// 원시데이터 : String, Number, Boolean, undefined, null :: js에서 사용하는 기본 데이터 :: 값이 다르면 다른녀석, 같으면 같은 녀석
// 참조형 데이터 : Object, Array, Function(callback) :: 불변성이 없고 가변성이다.
// 메모리 영역
// ------------------------------------------------------------------------------------------------------
// |1:      1      |2:     4       |3:            |4:
// ------------------------------------------------------------------------------------------------------